def sanitize_matches(matches):
    """Sanitizes the team slugs in the match data."""
    for match in matches:
        if match['homeSlug'] is not None:
            match['homeSlug'] = sanitize_slug(match['homeSlug'])
        else:
            match['homeSlug'] = sanitize_slug(match['homeName'])
        if match['awaySlug'] is not None:
            match['awaySlug'] = sanitize_slug(match['awaySlug'])
        else:
            match['awaySlug'] = sanitize_slug(match['awayName'])
    return matches

def sanitize_slug(slug):
    """Sanitizes a team slug by converting it to lowercase and replacing spaces with hyphens."""
    sanitized =  slug.lower().replace(" ", "-") if slug else None
    return sanitized



